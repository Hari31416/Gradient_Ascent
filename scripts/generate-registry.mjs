import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const BLOG_DATA_DIR = path.join(__dirname, '../data/blog')
const REGISTRY_FILE = path.join(__dirname, '../components/AnimationRegistry.tsx')

function findTsxFiles(dir, fileList = []) {
  const files = fs.readdirSync(dir)
  files.forEach((file) => {
    const filePath = path.join(dir, file)
    if (fs.statSync(filePath).isDirectory()) {
      findTsxFiles(filePath, fileList)
    } else if (file.endsWith('.tsx')) {
      fileList.push(filePath)
    }
  })
  return fileList
}

function generateRegistry() {
  console.log('Generating Animation Registry...')
  const tsxFiles = findTsxFiles(BLOG_DATA_DIR)
  
  const imports = []
  const registrations = []

  tsxFiles.forEach((file) => {
    const componentName = path.basename(file, '.tsx')
    // Get relative path from components/ dir
    let relativePath = path.relative(path.dirname(REGISTRY_FILE), file)
    // Fix extension and path prefix
    relativePath = relativePath.replace('.tsx', '')
    if (!relativePath.startsWith('.')) relativePath = './' + relativePath

    imports.push(`import ${componentName} from '${relativePath}'`)
    registrations.push(`  ${componentName},`)
  })

  const content = `// This file is auto-generated by scripts/generate-registry.mjs
// Do not edit this file manually.

${imports.join('\n')}

export const animationComponents = {
${registrations.join('\n')}
}
`

  fs.writeFileSync(REGISTRY_FILE, content)
  console.log(`Successfully generated registry with ${registrations.length} components.`)
}

generateRegistry()
